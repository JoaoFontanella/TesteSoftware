

public class Calculos {
    public static void main(String[] args) {
        float a, b, c, d;
        a = (2 - 1 + 2 + 1 + 3) - 3;
        System.out.println("A = " + a);
        b = ((2 * 3) * 5) - 4;
        System.out.println("B = " + b);
        c = (((2 + 6) * 9) - 3) / 7;
        System.out.println("C = " + c);
        d = (3 / 4) - 8;
        System.out.println("D = " + d);
    }
}
